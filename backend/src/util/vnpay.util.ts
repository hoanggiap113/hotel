export function parseVNPayDate(vnpayDate: number | string): Date {
  const dateStr = String(vnpayDate);

  // Parse: yyyyMMddHHmmss
  const year = parseInt(dateStr.substring(0, 4));
  const month = parseInt(dateStr.substring(4, 6)) - 1; // Month bắt đầu từ 0
  const day = parseInt(dateStr.substring(6, 8));
  const hour = parseInt(dateStr.substring(8, 10));
  const minute = parseInt(dateStr.substring(10, 12));
  const second = parseInt(dateStr.substring(12, 14));

  return new Date(year, month, day, hour, minute, second);
}

export function formatDateToVNPay(date: Date): number {
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const day = String(date.getDate()).padStart(2, '0');
  const hour = String(date.getHours()).padStart(2, '0');
  const minute = String(date.getMinutes()).padStart(2, '0');
  const second = String(date.getSeconds()).padStart(2, '0');

  return Number(`${year}${month}${day}${hour}${minute}${second}`);
}
